import { type EmailOtpType } from "@supabase/supabase-js";
import { NextResponse, type NextRequest } from "next/server";

import { createClient } from "@/lib/supabase/server";

/**
 * Verifikasi tautan email (undangan / reset password / konfirmasi) dengan
 * `token_hash` + verifyOtp. Alur ini aman untuk tautan yang dibuat admin
 * (invite) maupun reset password — tidak butuh code_verifier PKCE, sehingga
 * tidak lagi memunculkan "Tautan tidak valid atau kedaluwarsa".
 */
export async function GET(request: NextRequest) {
  const { searchParams, origin } = new URL(request.url);
  const tokenHash = searchParams.get("token_hash");
  const type = searchParams.get("type") as EmailOtpType | null;
  const next = searchParams.get("next") ?? "/pos";

  if (tokenHash && type) {
    const supabase = await createClient();
    const { error } = await supabase.auth.verifyOtp({ type, token_hash: tokenHash });
    if (!error) {
      return NextResponse.redirect(`${origin}${next}`);
    }
  }

  return NextResponse.redirect(`${origin}/login?error=auth`);
}
